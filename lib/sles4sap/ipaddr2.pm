=head2 ipaddr2_cloudinit_create

    my $cloud_init_file = ipaddr2_cloudinit_create();

Create the cloud-init.txt profile file

=over

=item B<scc_code> - Optional argument. If present, registration will be added in bootcmd 
                    section of the cloud-init profile file.
                    Having registration enabled is particularly important for BYOS images:
                    this profile also try to install nginx, this stage needs a previous registration.

=item B<nginx_root> - Optional argument: default is B</srv/www/htdocs>. This argument allow
                      to overwrite the location where the index.html, generated by the test and used to identify each node,
                      is stored. As the test is not using any nginx configuration file, this location has to match to the
                      default root folder configured by the particular version of nginx installed in the SUT.
                      Default value is/was value for nginx installable in 15sp5 and sp6.
                      12sp5 needs /usr/share/nginx/html

=item B<external_repo> - Optional argument: allow to add a PackageHub product

=back
=cut

sub ipaddr2_cloudinit_create(%args) {
    $args{nginx_root} //= '/srv/www/htdocs';

    # start to dynamically compose the profile
    # 1. Header
    # 2. disable initial zypper up, the update is done later by a dedicated test module,
    #    after adding repo for package we like to test
    # 3. socat is needed but not installed by default in 12sp5
    my $cloud_init_content = <<END;
#cloud-config
package_upgrade: false
packages:
  - socat
END
    # 4. Install and configure nginx, two variants as
    #    12sp5 does not have the nginx package
    if ($args{external_repo}) {
        $cloud_init_content .= <<END;
runcmd:
  - 'sudo SUSEConnect --product PackageHub/$args{external_repo}/x86_64'
  - 'sudo zypper ref'
  - 'sudo zypper install -y nginx'
END
    } else {
        $cloud_init_content .= <<END;
  - nginx
runcmd:
END
    }
    $cloud_init_content .= <<END;
  - 'echo "I am \$(hostname)" > $args{nginx_root}/index.html'
END
    # 3. Optionally add registration
    if ($args{scc_code}) {
        $cloud_init_content .= <<END;
bootcmd:
  - registercloudguest --clean
  - registercloudguest --force-new -r $args{scc_code}
END
    }
    my $cloud_init_file = '/tmp/cloud-init-web.txt';
    write_sut_file($cloud_init_file, $cloud_init_content);
    # upload to allow debugging
    upload_logs($cloud_init_file);
    return $cloud_init_file;
}

=head2 ipaddr2_infra_deploy

    my $rg = ipaddr2_infra_deploy();

Create a deployment in Azure designed for this specific test.

1. Create a resource group to contain all
2. Create a vnet and subnet in it
3. Create one Public IP
4. Create 2 VM to run the cluster, both running a web server and that are behind the LB
5. Create 1 additional VM that get
6. Create a Load Balancer with 2 VM in backend and with an IP as frontend

=over

=item B<region> - existing resource group

=item B<os> - existing Load balancer NAME

=item B<diagnostic> - enable diagnostic features if 1

=item B<cloudinit_profile> - filename of the cloud-init profile. Usually this one is the return value of
                      B<ipaddr2_cloudinit_create>.
                      Presence of this optional argument also enable cloud-init features in various deployment stages.

=item B<trusted_launch> - Enable or disable Trusted Launch. Default 1: Enabled.
                          If configured to 0 the result in az vm create is executed with '--security-type Standard'

=back
=cut

sub ipaddr2_infra_deploy(%args) {
    foreach (qw(region os)) {
        croak("Argument < $_ > missing") unless $args{$_}; }
    $args{diagnostic} //= 0;
    $args{trusted_launch} //= 1;

    az_version();

    my $rg = ipaddr2_azure_resource_group();

    az_group_create(
        name => $rg,
        region => $args{region});

    # If image provided is a blob storage link, create image out of it
    if ($args{os} =~ /\.vhd$/) {
        my $img_name = $rg . 'img';
        az_img_from_vhd_create(
            resource_group => $rg,
            name => $img_name,
            source => $args{os});
        $args{os} = $img_name;
    }

    # Create a VNET only needed later when creating the VM
    # Use $rg instead of DEPLOY_PREFIX to try to prevent
    # certain deployment failures like:
    #    Subnet(ip2t-snet) does not exist, but failed to create a new subnet
    #    with address prefix 10.0.0.0/24.
    my $vnet = "$rg-vnet";
    my $subnet = "$rg-snet";
    az_network_vnet_create(
        resource_group => $rg,
        region => $args{region},
        vnet => $vnet,
        address_prefixes => $priv_net_address_range{main_address_range},
        snet => $subnet,
        subnet_prefixes => $priv_net_address_range{subnet_address_range});

    # Create a Network Security Group
    # only needed later when creating the VM
    my $nsg = DEPLOY_PREFIX . '-nsg';
    az_network_nsg_create(
        resource_group => $rg,
        name => $nsg);

    # Create a public IP for external test access.
    # It is later assigned to the third VM (bastion role)
    az_network_publicip_create(
        resource_group => $rg,
        name => $bastion_pub_ip,
        sku => 'Standard',
        allocation_method => 'Static');

    # Create a public IP for the NAT Gateway
    az_network_publicip_create(
        resource_group => $rg,
        name => $nat_pub_ip,
        sku => 'Standard',
        allocation_method => 'Static');

    # Create the NAT Gateway
    my $nat_name = DEPLOY_PREFIX . '-nat';
    az_network_nat_gateway_create(
        resource_group => $rg,
        region => $args{region},
        name => $nat_name,
        public_ip => $nat_pub_ip);

    # Associate one of the Public IP to the NAT Gateway
    az_network_vnet_subnet_update(
        resource_group => $rg,
        vnet => $vnet,
        snet => $subnet,
        nat_gateway => $nat_name);

    # Create the load balancer entity.
    # Think about it as a "group" definition
    # to link back-end (2 VMs) and front-end (the Pub IP) resources
    # SKU Standard (and not Basic) is needed to get some Metrics
    my $lb = DEPLOY_PREFIX . '-lb';
    my $lb_be = DEPLOY_PREFIX . '-backend_pool';
    my $lb_fe = DEPLOY_PREFIX . '-frontend_ip';
    az_network_lb_create(
        resource_group => $rg,
        name => $lb,
        vnet => $vnet,
        snet => $subnet,
        backend => $lb_be,
        frontend_ip_name => $lb_fe,
        fip => $frontend_ip,
        sku => 'Standard');

    # All the 2 VM are later assigned to it.
    # The load balancer does not explicitly knows about it
    my $as = DEPLOY_PREFIX . '-as';
    az_vm_as_create(
        resource_group => $rg,
        name => $as,
        region => $args{region},
        fault_count => 2);
    # Next two lines are for debug purpose only:
    # in time to time next vm create fails for missing AS
    az_vm_as_list(resource_group => $rg);
    az_vm_as_show(
        resource_group => $rg,
        name => $as);

    my $storage_name;
    if ($args{diagnostic}) {
        $storage_name = $storage_account . get_current_job_id();
        az_storage_account_create(
            resource_group => $rg,
            region => $args{region},
            name => $storage_name);
    }

    # - Create 2 VMs
    # - for each of them open port 80
    # - link their NIC/ipconfigs to the load balancer to be managed
    my $vm;
    my %vm_create_generic_args = (
        resource_group => $rg,
        region => $args{region},
        image => $args{os},
        username => USER,
        vnet => $vnet,
        snet => $subnet,
        ssh_pubkey => get_ssh_private_key_path() . '.pub',
        public_ip => "");
    if (!$args{trusted_launch}) {
        $vm_create_generic_args{security_type} = 'Standard';
    }

    my %vm_create_internal_args = %vm_create_generic_args;
    $vm_create_internal_args{availability_set} = $as;
    $vm_create_internal_args{nsg} = $nsg;
    $vm_create_internal_args{public_ip} = "";
    $vm_create_internal_args{custom_data} = $args{cloudinit_profile} if ($args{cloudinit_profile});

    foreach my $i (1 .. 2) {
        $vm = ipaddr2_get_internal_vm_name(id => $i);
        # the VM creation command refers to an external cloud-init
        # configuration file that is in charge to install and setup
        # the nginx server.
        $vm_create_internal_args{name} = $vm;
        az_vm_create(%vm_create_internal_args);

        if ($args{diagnostic}) {
            az_vm_diagnostic_log_enable(resource_group => $rg,
                storage_account => $storage_name,
                vm_name => $vm);
        }

        if ($args{cloudinit_profile}) {
            az_vm_wait_cloudinit(
                resource_group => $rg,
                name => $vm,
                username => USER);
        }

        az_vm_openport(
            resource_group => $rg,
            name => $vm, port => 80);
    }

    my %vm_create_bastion_args = %vm_create_generic_args;
    $vm_create_bastion_args{name} = $bastion_vm_name;
    $vm_create_bastion_args{public_ip} = $bastion_pub_ip;
    az_vm_create(%vm_create_bastion_args);

    # Keep this loop separated from the other to hopefully
    # give cloud-init more time to run and avoid interfering
    # with it by changing the networking on the running VM
    foreach my $i (1 .. 2) {
        my $vm = ipaddr2_get_internal_vm_name(id => $i);
        my $nic_id = az_nic_get_id(
            resource_group => $rg,
            name => $vm);
        my $ip_config = az_ipconfig_name_get(nic_id => $nic_id);
        my $nic_name = az_nic_name_get(nic_id => $nic_id);

        # Change the IpConfig to use a static IP:
        # https://documentation.suse.com/sle-ha/15-SP5/html/SLE-HA-all/article-installation.html#vl-ha-inst-quick-req-other
        az_ipconfig_update(
            resource_group => $rg,
            ipconfig_name => $ip_config,
            nic_name => $nic_name,
            ip => ipaddr2_get_internal_vm_private_ip(id => $i));

        # Add the IpConfig to the LB pool
        az_ipconfig_pool_add(
            resource_group => $rg,
            lb_name => $lb,
            address_pool => $lb_be,
            ipconfig_name => $ip_config,
            nic_name => $nic_name,
            timeout => 300);
    }

    # Health probe is using the port exposed by the cluster RA azure-lb
    # to understand if each of the VM in the cluster is OK.
    # It is eventually the cluster itself that
    # cares to monitor the below service (port 80)
    my $lb_health_port_name = $lb . "_health";
    my $lb_health_port = '62500';
    az_network_lb_probe_create(
        resource_group => $rg,
        lb_name => $lb,
        name => $lb_health_port_name,
        port => $lb_health_port);

    # Configure the load balancer behavior
    az_network_lb_rule_create(
        resource_group => $rg,
        lb_name => $lb,
        hp_name => $lb_health_port_name,
        backend => $lb_be,
        frontend_ip => $lb_fe,
        name => $lb . "_rule",
        port => '80');

    foreach (1 .. 2) {
        my $wt = az_vm_wait_running(
            resource_group => $rg,
            name => ipaddr2_get_internal_vm_name(id => $_),
            timeout => 1200
        );
        record_info('VM RUNNING', "VM $_ takes $wt seconds to reach the Running state.");
    }
}

=head2 ipaddr2_bastion_pubip

    my $bastion_ip = ipaddr2_bastion_pubip();

Get the only public IP in the deployment associated to the VM used as bastion. Function is getting
the IP address using az cli, so it could die if the az cli fails to return a valid IP.
