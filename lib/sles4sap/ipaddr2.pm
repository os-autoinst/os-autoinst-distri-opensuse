sub ipaddr2_bastion_pubip {
    my $rg = ipaddr2_azure_resource_group();
    my $pub_ip = az_network_publicip_get(resource_group => $rg, name => $bastion_pub_ip);
    my $ip_obj = NetAddr::IP::Lite->new($pub_ip) or die "Invalid IP '$pub_ip'";
    return $ip_obj->addr;
}

